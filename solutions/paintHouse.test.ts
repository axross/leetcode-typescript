import minCost from "./paintHouse";

describe("256. Paint House", () => {
  test("#1", () => {
    expect(minCost([[17, 2, 17], [16, 16, 5], [14, 3, 19]])).toBe(10);
  });

  test("#2", () => {
    expect(
      minCost([[17, 2, 17], [16, 16, 5], [30, 3, 19], [4, 8, 2], [10, 15, 11]])
    ).toBe(22);
  });

  // 5+13+5+14+10 = 47
  // 5+13+7+15+3 = 43
  // 5+13+5+17+3 = 43
  test("#3", () => {
    expect(
      minCost([[5, 8, 6], [19, 14, 13], [7, 5, 12], [14, 15, 17], [3, 20, 10]])
    ).toBe(43);
  });

  // 5+13+7+15+3 = 43
  // 5+13+5+20+3 = 46
  // 5+13+5+14+10 = 47
  test("#4", () => {
    expect(
      minCost([[5, 8, 6], [19, 14, 13], [7, 5, 12], [14, 15, 20], [3, 20, 10]])
    ).toBe(43);
  });
});
